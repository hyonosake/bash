#include "libft.h"

size_t		ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	diff;

	diff = dst - src;
	if (dst < src)
		diff = src - dst;
	i = 0;
	if (!dst || !src)
		return (0);
	if (dstsize == 0 || diff < dstsize)
		return (ft_strlen(src));
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ft_strlen(src));
}
